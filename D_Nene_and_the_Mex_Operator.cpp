ll n, a[N], dp[30], b[N];

ll Calc(int i)
{
    if (i > n)
    {
        return 0;
    }
    ll &res = dp[i];
    if (res != -1)
    {
        return res;
    }
    res = a[i] + Calc(i + 1);
    for (ll j = i; j <= n; ++j)
    {
        res = max(res, (j - i + 1) * (j - i + 1) + Calc(j + 1));
    }
    return res;
}

vector<pll> Operations;

void Make(int l, int r, int Mex) /// will make 3 2 1 0
{
    if (l > r)
        return;
    if (a[l])
    {
        Operations.push_back({l, l});
        a[l] = 0; /// will make 0 x x x
    }
    if (l == r)
    {
        return; /// will make 0 x x x
    }
    Make(l + 1, r, Mex - 1); /// will make 0 2 1 0

    Operations.push_back({l, r});
    for (int i = l; i <= r; i++)
    {
        a[i] = Mex; /// will make 3 3 3 3
    }
    Make(l + 1, r, Mex - 1); /// finally we have 3 2 1 0
}

int32_t main()
{
    fast;
    cin>>n;

    for (int i = 1; i <= n; i++)
    {
        cin >> a[i];
        b[i] = a[i];
    }

    memset(dp, -1, sizeof(dp));

    int ans = Calc(1);

    cout << ans << ' ';

    for (int i = 1; i <= n; i++)
    {
        if (Calc(i + 1) + b[i] == ans)
        {
            ans -= b[i];
        }
        else
        {
            for (int j = i; j <= n; ++j)
            {
                if ((j - i + 1) * (j - i + 1) + Calc(j + 1) == ans)
                {
                    ans -= (j - i + 1) * (j - i + 1);
                    Make(i, j, (j - i + 1));
                    Operations.push_back({i, j});
                    i = j;
                    break;
                }
            }
        }
    }

    cout << Operations.size() << endl;

    for (auto p : Operations)
    {
        cout <<p<<nn;
    }

    return 0;
}
