vector<ll> g[30];
ll n;
string s, p;

bool vis[30];
ll level[30];

// bfs=breadth first search
// according to level traversal

void bfs(ll source)
{
    mem(level, M);
    mem(vis, 0);
    queue<ll> q;
    q.push(source);
    vis[source] = 1;
    level[source] = 0;
    while (!q.empty())
    {
        ll curr_v = q.front();
        q.pop();
        // cout<<curr_v<<" ";
        for (ll child : g[curr_v])
        {
            if (!vis[child])
            {
                q.push(child);
                vis[child] = 1;
                level[child] = level[curr_v] + 1;
            }
        }
    }
    // cout<<endl;
}

